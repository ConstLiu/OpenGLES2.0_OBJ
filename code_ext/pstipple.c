  /* GLUT초기화 */
  glutInit(&argc, argv);  
  glutInitDisplayMode(GLUT_SINGLE | GLUT_RGBA);  
  glutInitWindowPosition(5,5);  
  glutInitWindowSize(300,300);
  
  /*창을 하나 연다. */
  glutCreateWindow("Stipple");  
  
